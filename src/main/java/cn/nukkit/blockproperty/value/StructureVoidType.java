package cn.nukkit.blockproperty.value;

import cn.nukkit.api.PowerNukkitOnly;
import cn.nukkit.api.Since;

/**
 * Determines which void mode to draw for structure blocks.
 */
@PowerNukkitOnly
@Since("1.4.0.0-PN")
public enum  StructureVoidType {
    @PowerNukkitOnly
    @Since("1.4.0.0-PN")
    VOID,
    
    @PowerNukkitOnly
    @Since("1.4.0.0-PN")
    AIR
}
